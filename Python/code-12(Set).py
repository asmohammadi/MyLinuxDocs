a = set() # Set will remove the duplicated items
print(a)

# Add item to Set:
a = set()
a.add(4)
a.add(1)
print(a)

# Unique items:
a = set()
a.add(4)
a.add(1)
a.add(2)
a.add(1)
print(a)

# Example:
my_list = [1,2,3,4,5]
a = set(my_list)
print(a)

# Unique items:
my_list = [1,2,3,4,5,6,6,6,1,2,3,2,1,10]
a = set(my_list)
print(a)

